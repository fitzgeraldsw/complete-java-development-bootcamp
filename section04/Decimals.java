public class Decimals {
    public static void main(String[] args) {
        double percentage = 55.70;
        System.out.printf("in 2020, %.2f%% of the world's population is urban.%n", percentage);
    }
}

// int is a primitive type, not a class.  The corresponding class type is Integer spelled according to the class naming conventions.
// String is a class, not a primitive type. 

// Similar pairs of primitive and class types are
// byte vs Byte
// short vs Short
// long vs Long
// float vs Float
// double vs Double
// boolean vs Boolean
// char vs Character

// % = modulus