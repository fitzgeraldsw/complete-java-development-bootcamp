public class WholeNumbers {
    public static void main(String[] args) {

        // max int = 2 billion = 4 bytes = 2^31 = 0-31 bits = 32 bits
        int numberOfFans = 5000;
        System.out.println("There are " + numberOfFans + " fans at the Quidditch match.");

        // max long = 9 quintillion = 8 bytes = 2^63 = 0-63 bits = 64 bits
        long globalPopulation = 7000000000L;
        System.out.println("There are " + globalPopulation + " people in the world.");
    }
}
